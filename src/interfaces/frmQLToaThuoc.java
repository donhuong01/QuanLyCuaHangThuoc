    private void btnthemvaotoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemvaotoaActionPerformed
        // TODO add your handling code here:).log(Level.SEVERE, null, ex);
        ResultSet reset = null;
        DuocPhamContronler dp = new DuocPhamContronler();

        int soluong = Integer.parseInt(txtsoluong.getText());
        int index = tbdanhsachdp.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tbdanhsachdp.getModel();
        String madp = model.getValueAt(index, 0).toString();
        String tendp = model.getValueAt(index, 1).toString();
        double giaban = Double.parseDouble(model.getValueAt(index, 7).toString());
        double thanhtien = soluong * giaban;

        DefaultTableModel modeltb = (DefaultTableModel) tbduocpham.getModel();
        try {
            reset = dp.GetSoLuong(madp);
            while (reset.next()) {
                if (soluong > reset.getInt("soluong")) {
                    JOptionPane.showMessageDialog(null, "Số lượng lấy ra phải nhỏ hơn số lượng hiện có");
                } else if (reset.getInt("soluong") == 0) {
                    JOptionPane.showMessageDialog(null, "Số lượng hiện có = 0");
                } else {
                    dp.SetSoLuong(madp, reset.getInt("soluong") - soluong);

                    modeltb.insertRow(0, new Object[]{madp, tendp, soluong, giaban, thanhtien});
                    txtthanhtien.setText("");
                }
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        } catch (IOException ex) {
            Logger.getLogger(frmQLToaThuoc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnthemvaotoaActionPerformed

