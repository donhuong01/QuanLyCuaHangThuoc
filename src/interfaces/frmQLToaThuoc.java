    private boolean checkFieldsOK() {
        txtngayke.setText(txtngayke.getText().trim());
        if (txtngayke.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập ngày kê đơn");
            return false;
        }
        if (txtthanhtien.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa hiển thị tổng tiền thuốc");
            return false;
        }
        try {
            if (Double.parseDouble(txtthanhtien.getText()) <= 0) {
                throw new Exception();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tổng tiền phải là số > 0");
            return false;
        }
        return true;
    }
    private void btnloaiboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloaiboActionPerformed
        // TODO add your handling code here:
        int row = tbduocpham.getSelectedRow();
        DefaultTableModel modeltb = (DefaultTableModel) tbduocpham.getModel();

        ResultSet reset = null;
        DuocPhamContronler dp = new DuocPhamContronler();
        String madp = modeltb.getValueAt(row, 0).toString();
        String soluong = modeltb.getValueAt(row, 2).toString();

        try {
            reset = dp.GetMa(madp);
            while (reset.next()) {
                reset = dp.GetSoLuong(madp);
                while (reset.next()) {
                    dp.SetSoLuong(madp, reset.getInt("soluong") + Integer.parseInt(soluong));

                    modeltb.removeRow(row);
                    txtthanhtien.setText("");
                }
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        } catch (IOException ex) {
            Logger.getLogger(frmQLToaThuoc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnloaiboActionPerformed

