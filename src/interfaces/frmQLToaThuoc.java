    private void btntinhtienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntinhtienActionPerformed
        // TODO add your handling code here:
        int n = tbduocpham.getRowCount();
        double[] arr = new double[n];
        String tien;
        double tongtienthuoc = 0;

        for (int i = 0; i < tbduocpham.getColumnCount(); i++) {
            for (int j = 0; j < tbduocpham.getRowCount(); j++) {
                if (i == 4) {
                    Object tienthuoc = tbduocpham.getValueAt(j, i);
                    if (tbduocpham.getValueAt(j, i) != null) {
                        tien = tienthuoc.toString();
                        arr[i] = Double.parseDouble(tien);
                        tongtienthuoc += arr[i];
                    }
                }
            }
        }
        txtthanhtien.setText(Double.toString(tongtienthuoc));
    }//GEN-LAST:event_btntinhtienActionPerformed

