    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        PhieuNhapContronler pn = new PhieuNhapContronler();
        int chondong = 0;
        chondong = tbhienthi.getSelectedRow();
        String sopn = tbhienthi.getValueAt(chondong, 0) + " ";
        int ret = JOptionPane.showConfirmDialog(this, "Do you want to delete?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (ret != JOptionPane.YES_OPTION) {
            return;
        } else {
            try {
                pn.Delete(sopn);
                DefaultTableModel model = (DefaultTableModel) tbhienthi.getModel();
                model.setRowCount(0);
                ResultSet reset = pn.GetData();
                int i = 0;
                int k = 1;
                while (reset.next()) {
                    model.setRowCount(++k);
                    tbhienthi.setValueAt(reset.getString("sophieunhap"), i, 0);
                    tbhienthi.setValueAt(reset.getString("tenduocpham"), i, 1);
                    tbhienthi.setValueAt(reset.getString("ngaynhap"), i, 2);
                    tbhienthi.setValueAt(reset.getString("solo"), i, 3);
                    tbhienthi.setValueAt(reset.getString("soluong"), i, 4);
                    tbhienthi.setValueAt(reset.getString("donvitinh"), i, 5);
                    tbhienthi.setValueAt(reset.getString("ngaysanxuat"), i, 6);
                    tbhienthi.setValueAt(reset.getString("hansudung"), i, 7);
                    tbhienthi.setValueAt(reset.getString("tennhacc"), i, 8);
                    tbhienthi.setValueAt(reset.getString("gianhap"), i, 9);
                    i++;
                }
            } catch (SQLException ex) {
                System.out.println(ex.toString());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

