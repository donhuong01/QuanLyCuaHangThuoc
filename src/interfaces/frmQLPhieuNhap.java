    private void butNhapHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butNhapHangActionPerformed
        ResultSet reset = null;
        PhieuNhapContronler a = new PhieuNhapContronler();
        DuocPhamContronler dp = new DuocPhamContronler();

        if (checkInputOK()) {
            String tendp = txtduocpham.getText();
            String ngaynhap = txtngaynhap.getText();
            int solo = Integer.parseInt(txtsolo.getText());
            String dvitinh = txtdvitinh.getText();
            double gianhap = Double.parseDouble(txtgianhap.getText());
            String ngaysx = txtngaysx.getText();
            String hansd = txthsd.getText();
            int soluong = Integer.parseInt(txtsoluong.getText());
            String nhacc = cbonhacc.getSelectedItem().toString();

            try {
                reset = dp.GetSoLuong(tendp);

                ResultSet reset1 = null;
                while (reset.next()) {
                    reset1 = dp.GetDuTruToiThieu(tendp);
                    int kiemtraSL = reset.getInt("soluong") + soluong;
                    while (reset1.next()) {
                        if (reset1.getInt("dutrutoida") <= kiemtraSL) {
                            JOptionPane.showMessageDialog(null, "Số lượng thêm vào có tổng phải nhỏ hơn số lượng tối đa");
                        } else {
                            dp.SetSoLuong(tendp, soluong + reset.getInt("soluong"));

                            a.Insert(tendp, ngaynhap, solo, soluong, dvitinh, ngaysx, hansd, nhacc, gianhap);

                            DefaultTableModel model = (DefaultTableModel) tbhienthi.getModel();
                            model.setRowCount(0);
                            reset = a.GetData();
                            int i = 0;
                            int k = 1;
                            while (reset.next()) {
                                model.setRowCount(++k);
                                tbhienthi.setValueAt(reset.getString("sophieunhap"), i, 0);
                                tbhienthi.setValueAt(reset.getString("tenduocpham"), i, 1);
                                tbhienthi.setValueAt(reset.getString("ngaynhap"), i, 2);
                                tbhienthi.setValueAt(reset.getString("solo"), i, 3);
                                tbhienthi.setValueAt(reset.getString("soluong"), i, 4);
                                tbhienthi.setValueAt(reset.getString("donvitinh"), i, 5);
                                tbhienthi.setValueAt(reset.getString("ngaysanxuat"), i, 6);
                                tbhienthi.setValueAt(reset.getString("hansudung"), i, 7);
                                tbhienthi.setValueAt(reset.getString("tennhacc"), i, 8);
                                tbhienthi.setValueAt(reset.getString("gianhap"), i, 9);
                                i++;
                            }
                        }
                    }
                }
            } catch (SQLException ex) {
                System.out.println(ex.toString());
            } catch (IOException ex) {
                Logger.getLogger(frmQLPhieuNhap.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_butNhapHangActionPerformed

