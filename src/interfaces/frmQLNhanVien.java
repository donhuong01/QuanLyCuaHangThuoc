    private boolean checkInputOK() {
        txthoten.setText(txthoten.getText().trim());
        txtntns.setText(txtntns.getText().trim());
        txtgtinh.setText(txtgtinh.getText().trim());
        txtsdt.setText(txtsdt.getText().trim());
        txtemail.setText(txtemail.getText().trim());
        txtdiachi.setText(txtdiachi.getText().trim());
        txtpass.setText(txtpass.getText().trim());
        if (txthoten.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tên nhân viên");
            return false;
        }
        if (txtgtinh.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập giới tính");
            return false;
        }
        if (txtntns.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập ngày thàng năm sinh");
            return false;
        }
        if (txtemail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập email nhân viên");
            return false;
        }
        if (txtsdt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập số điện thoại");
            return false;
        }
        if (txtdiachi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập địa chỉ");
            return false;
        }
        if (txtpass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập password nhân viên");
            return false;
        }
        return true;
    }

