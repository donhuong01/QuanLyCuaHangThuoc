    private void butModeCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butModeCapNhatActionPerformed
        NhanVienContronler a = new NhanVienContronler();
        int chondong = 0;
        chondong = tbhienthi.getSelectedRow();
        String manv = tbhienthi.getValueAt(chondong, 0) + "";
        String user = cbouser.getSelectedItem().toString();
        String tennv = txthoten.getText();
        String date = txtntns.getText();
        String gender = txtgtinh.getText();
        String email = txtemail.getText();
        String sodt = txtsdt.getText();
        String diachi = txtdiachi.getText();
        String pass = txtpass.getText();
        try {
            a.Update(manv, user, tennv, date, gender, sodt, email, diachi, pass);
            DefaultTableModel model = (DefaultTableModel) tbhienthi.getModel();
            model.setRowCount(0);
            ResultSet reset = a.GetData();
            int i = 0;
            int k = 1;
            while (reset.next()) {
                model.setRowCount(++k);
                tbhienthi.setValueAt(reset.getString("maNV"), i, 0);
                tbhienthi.setValueAt(reset.getString("username"), i, 1);
                tbhienthi.setValueAt(reset.getString("name"), i, 2);
                tbhienthi.setValueAt(reset.getString("dateofbirth"), i, 3);
                tbhienthi.setValueAt(reset.getString("gender"), i, 4);
                tbhienthi.setValueAt(reset.getString("phone"), i, 5);
                tbhienthi.setValueAt(reset.getString("email"), i, 6);
                tbhienthi.setValueAt(reset.getString("address"), i, 7);
                tbhienthi.setValueAt(reset.getString("password"), i, 8);
                i++;
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_butModeCapNhatActionPerformed

